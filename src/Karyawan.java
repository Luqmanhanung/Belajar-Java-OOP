public class Karyawan {
    private String nama;
    private int gaji;
    private int usia;

    public String getNama(){
        return nama;
    }

    public void setNama (String nama){
        this.nama = nama;
    }

    public int getGaji(){
        return gaji;
    }

    public void setGaji (int gaji){
        this.gaji = gaji;
    }

    public int getUsia(){
        return usia;
    }

    public void setUsia (int usia){
        this.usia = usia;
    }
}
